# Operadores de Comparación
# Usamos operadores y nos devuelven valores BOOLEANOS
numA = 18
numB = 5

# Mayor que
resultado = numA > numB
print("Mayor que: ¿Es 18 > que 5?", resultado)

resultado2 = numA >= numB
print("Mayor o igual que: ¿Es 18 mayor o igual que 5?", resultado2)

# Menor que
resultado3 = numA < numB
print("Menor que: ¿Es 18 > que 5?", resultado3)

resultado4 = numA <= numB
print("Menor o igual que: ¿Es 18 menor o igual que 5?", resultado4)


#Igualdad
resultado = numA == numB
print("¿Es igual numA(18) a numB(5)?", resultado)

#Distinto
resultado = numA != numB
print("¿Es 18 distinto de 5?", resultado)


